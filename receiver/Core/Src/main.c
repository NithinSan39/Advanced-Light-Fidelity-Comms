/* USER CODE BEGIN Includes */
#include "ssd1306.h"
#include "ssd1306_fonts.h"
/* USER CODE END Includes */

/* USER CODE BEGIN PV */
volatile uint32_t last_edge_time = 0; // Remembers when the light last changed
volatile uint8_t rx_char = 0;         // Builds the decoded letter bit-by-bit
volatile uint8_t bit_count = 0;       // Counts up to 8 bits
volatile uint8_t char_ready = 0;      // Flag to tell the OLED to print

uint8_t cursor_x = 2;                 // Tracks horizontal OLED printing position
uint8_t cursor_y = 35;                // Tracks vertical OLED printing position
/* USER CODE END PV */

/* Private variables ---------------------------------------------------------*/
I2C_HandleTypeDef hi2c1;
TIM_HandleTypeDef htim3;

/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_I2C1_Init(void);
static void MX_TIM3_Init(void);

/* USER CODE BEGIN 0 */
/* USER CODE END 0 */

int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_TIM3_Init();

  /* USER CODE BEGIN 2 */
  HAL_TIM_Base_Start(&htim3); // Start the 1-microsecond stopwatch (No IT!)

  ssd1306_Init();             // Wake up the OLED
  ssd1306_Fill(Black);
  ssd1306_SetCursor(15, 10);
  ssd1306_WriteString("Li-Fi Active", Font_7x10, White);
  ssd1306_UpdateScreen();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
      if (char_ready == 1) {
          char temp_str[2] = {rx_char, '\0'};

          ssd1306_SetCursor(cursor_x, cursor_y);
          ssd1306_WriteString(temp_str, Font_7x10, White);
          ssd1306_UpdateScreen();

          cursor_x += 11;

          if (cursor_x > 115) {
              cursor_x = 2;
              ssd1306_Fill(Black);
              ssd1306_SetCursor(15, 10);
              ssd1306_WriteString("Li-Fi Active", Font_7x10, White);
          }
          char_ready = 0;
      }
  }
  /* USER CODE END 3 */
}

void SystemClock_Config(void)
{
  RCC_OscInitTypeDef RCC_OscInitStruct = {0};
  RCC_ClkInitTypeDef RCC_ClkInitStruct = {0};

  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.HSEPredivValue = RCC_HSE_PREDIV_DIV1;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL9;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV2;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_2) != HAL_OK)
  {
    Error_Handler();
  }
}

